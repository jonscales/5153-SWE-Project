  @override
  void initState() {
    super.initState();
    _loadCustomMarker();
    _getUserLocation();
    _startLocationUpdates(); // starts the location tracking
  }

  Set<gmap.Marker> _markers = {};
  StreamSubscription<Position>? positionStream;
  gmap.Marker? userLocationMarker;
  late gmap.GoogleMapController myLocationEnabled;
  late gmap.BitmapDescriptor? customIcon;
  late gmap.GoogleMapController _mapController;
  static var currentPosition =
      gmap.LatLng(33.91838404142354, -98.4973221596422);

  bool mapIsBeingMoved = false;

  void _getUserLocation() async {
    var position = await GeolocatorPlatform.instance
        .getCurrentPosition(locationSettings: const LocationSettings());

    setState(() {
      currentPosition = gmap.LatLng(position.latitude, position.longitude);
      _markers.add(
        gmap.Marker(
          markerId: const gmap.MarkerId('user_location'),
          position: currentPosition,
          icon: customIcon ?? gmap.BitmapDescriptor.defaultMarker,
        ),
      );
    });
  }

  Future<void> _loadCustomMarker() async {
    customIcon = await gmap.BitmapDescriptor.fromAssetImage(
      const ImageConfiguration(size: Size(48, 48)),
      'assets/images/bike_marker.png',
    );
  }

  void _startLocationUpdates() async {
    bool serviceEnabled = await Geolocator.isLocationServiceEnabled();
    if (!serviceEnabled) return;

    LocationPermission permission = await Geolocator.checkPermission();
    if (permission == LocationPermission.denied) {
      permission = await Geolocator.requestPermission();
      if (permission == LocationPermission.denied) return;
    }
    if (permission == LocationPermission.deniedForever) return;

    final locationSettings = LocationSettings(
      accuracy: LocationAccuracy.high,
      distanceFilter: 100, // update every 100 meters
    );

    positionStream =
        Geolocator.getPositionStream(locationSettings: locationSettings)
            .listen((Position position) {
      final currentLatLng = gmap.LatLng(position.latitude, position.longitude);

      //added these lines base on chatgpt recommendation
      final updatedMarker = gmap.Marker(
        markerId: const gmap.MarkerId('user_location'),
        position: currentLatLng,
        icon: customIcon ?? gmap.BitmapDescriptor.defaultMarker,
      );

      setState(() {
        userLocationMarker = updatedMarker;
        _markers.removeWhere((m) => m.markerId.value == 'user_location');
        _markers.add(updatedMarker);
      });

      if (!mapIsBeingMoved) {
        _mapController.animateCamera(
          gmap.CameraUpdate.newLatLng(currentLatLng),
        );
      }
    });
  }

  @override
  void dispose() {
    positionStream?.cancel();
    super.dispose();
  }

