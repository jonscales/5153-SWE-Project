String? calculateAmountOwed(List<String> eventList) {
  int total = 0;

  for (final event in eventList) {
    final lower = event.toLowerCase();

    if (lower.contains('10 k child')) {
      total += 0;
    } else if (lower.contains('10 k adult')) {
      total += 50;
    } else {
      total += 75;
    }
  }

//format as text for currency display
  return '\$${total.toStringAsFixed(2)}';
}
