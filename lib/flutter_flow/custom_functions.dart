  var payTable = "";
  if (mainEvent == "Endurance Rides (25-100miles)" ||
      mainEvent == "10 K Adult" ||
      mainEvent == "10 K Child") {
    payTable = "Endurance Road Rides Area";
  } else if (mainEvent == "Grava del Fuego") {
    payTable = "Gravel Events Table";
  } else if (mainEvent == "Wee-Chi-Tah MTB Races") {
    payTable = "Dirt Events Table";
  } else if (mainEvent == "Wee-Chi-Tah Trail Run") {
    payTable = "Trail Run Events Table";
  }
  return payTable;
}

